
/* Set thời gian hiển thị loi - Index + Nhan Vien   */
document.addEventListener("DOMContentLoaded", function () {
    var errorMessage = document.getElementById("error-message");

    // Xử lý ẩn thông báo lỗi sau 800 milliseconds
    setTimeout(function () {
        errorMessage.style.display = "none"; // Ẩn thông báo lỗi sau 800 milliseconds
    }, 800); // 800 milliseconds
});
